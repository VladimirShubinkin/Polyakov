'''Исполнитель Нолик преобразует двоичное число, записанное на экране.
У исполнителя есть две команды, которым присвоены номера:
1. Вычесть 1
2. Убрать последнюю цифру справа
Первая команда уменьшает число на 1. Вторая команда убирает последнюю справа цифру,
например, для числа 110 результатом работы данной команды будет являться число 11.
Сколько существует программ, которые исходное двоичное число 100001 преобразуют
в двоичное число 100?
'''
def f(a, b):
    if a < b:
        return 0
    if a == b:
        return 1
    return f(a - 1, b) + f(a // 2, b)


print(f(int('100001', 2), int('100', 2)))
