'''
212)В файле 17-4.txt содержится последовательность целых чисел.
Элементы последовательности могут принимать целые значения от 0 до 10 000 включительно.
Определите количество пар, в которых хотя бы один из двух элементов больше,
чем среднее арифметическое всех чисел в файле, а их сумма оканчивается на 9.
В ответе запишите два числа: сначала количество найденных пар,
а затем – минимальную сумму элементов таких пар.
В данной задаче под парой подразумевается два идущих подряд элемента последовательности.
'''

with open('17-4.txt') as f:
    a = list(map(int, f.readlines()))

aver = sum(a) / len(a)
count = 0
min_sum = 10**9
for i in range(len(a) - 1):
    s = a[i] + a[i + 1]
    first = a[i] > aver or a[i + 1] > aver
    second = s % 10 == 9
    if first and second:
        min_sum = min(min_sum, s)
        count += 1
print(count, min_sum)
